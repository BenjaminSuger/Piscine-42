#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (0 < argc - i)
	{
		ft_putstr(argv[argc - i]);
		write(1, "\n", 1);
		i++;
	}
}
